# ---------------------------------------------------------------------------
# Driver
# ---------------------------------------------------------------------------


async def main() -> None:
    await init_db()
    async with SessionLocal() as db:
        # Directions ----------------------------------------------------------
        for name, desc in DIRECTIONS:
            existing = await db.scalar(select(Direction).where(Direction.name == name))
            if existing:
                existing.description = desc
            else:
                db.add(Direction(name=name, description=desc))
        await db.commit()

        # Courses -------------------------------------------------------------
        for c in COURSES:
            existing = await db.scalar(select(CustomCourse).where(CustomCourse.slug == c["slug"]))
            if existing:
                for k, v in c.items():
                    setattr(existing, k, v)
            else:
                db.add(CustomCourse(**c))
        await db.commit()

        # North scenarios ----------------------------------------------------
        for s in SCENARIOS:
            existing = await db.scalar(
                select(Scenario).where(Scenario.scenario_uid == s["scenario_uid"])
            )
            if existing:
                existing.name = s["name"]
                existing.directions = s["directions"]
                existing.course_tags = s["course_tags"]
                existing.steps = s["steps"]
                existing.status = "published"
            else:
                db.add(
                    Scenario(
                        scenario_uid=s["scenario_uid"],
                        name=s["name"],
                        department=s["directions"][0] if s["directions"] else "",
                        directions=s["directions"],
                        course_tags=s["course_tags"],
                        steps=s["steps"],
                        status="published",
                        created_at=datetime.utcnow(),
                    )
                )
        await db.commit()

    # Knowledge base files (on disk) ------------------------------------------
    settings.regulations_path.mkdir(parents=True, exist_ok=True)
    for filename, body in KB_FILES.items():
        path = settings.regulations_path / filename
        path.write_text(body, encoding="utf-8")
    # Refresh BM25 so the AI mentor can cite the new files immediately.
    rag_index.load_dir(settings.regulations_path)

    print("seeded:")
    print(f"  directions: {len(DIRECTIONS)}")
    print(f"  courses:    {len(COURSES)}")
    print(f"  scenarios:  {len(SCENARIOS)}")
    print(f"  kb files:   {len(KB_FILES)} -> {settings.regulations_path}")


if __name__ == "__main__":
    asyncio.run(main())
