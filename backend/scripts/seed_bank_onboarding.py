def _quiz(items):
    """Compact quiz factory: items is a list of (question, options) where
    options is a list of (text, is_correct) tuples."""
    out = []
    for i, (q, opts) in enumerate(items, 1):
        out.append({
            "id": f"q{i}",
            "question": q,
            "options": [
                {"id": chr(ord("a") + j), "text": text, "correct": bool(corr)}
                for j, (text, corr) in enumerate(opts)
            ],
            "explanation": "",
        })
    return out


